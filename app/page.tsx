"use client";

import { useEffect } from "react";
import { useRouter } from "next/navigation";

export default function Home() {
  const router = useRouter();

  useEffect(() => {
    const auth = localStorage.getItem('ai_labo_auth');
    if (auth) {
      router.push('/dashboard');
    } else {
      router.push('/login');
    }
  }, [router]);

  return <main />;
}
